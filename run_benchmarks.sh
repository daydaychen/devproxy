go test -bench=BenchmarkReuseBuffer -benchmem -run=^$ perf_optimization_test.go 2>/dev/null | grep "BenchmarkReuse"
go test -bench=BenchmarkBufferWithGrow -benchmem -run=^$ perf_optimization_test.go 2>/dev/null | grep "BenchmarkBuffer"
